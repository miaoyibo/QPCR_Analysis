package com.huoyan.model;

public class CriteriaModel {
	
	private Double fam;
	
	private Double vic;
	
	private String specialFamValue;
	
	private String specialVicValue;
	
	private String famSymbol;
	
	private String vicSymbol;
	

	public Double getFam() {
		return fam;
	}

	public void setFam(Double fam) {
		this.fam = fam;
	}

	public Double getVic() {
		return vic;
	}

	public void setVic(Double vic) {
		this.vic = vic;
	}

	public String getFamSymbol() {
		return famSymbol;
	}

	public void setFamSymbol(String famSymbol) {
		this.famSymbol = famSymbol;
	}

	public String getVicSymbol() {
		return vicSymbol;
	}

	public void setVicSymbol(String vicSymbol) {
		this.vicSymbol = vicSymbol;
	}

	public String getSpecialFamValue() {
		return specialFamValue;
	}

	public void setSpecialFamValue(String specialFamValue) {
		this.specialFamValue = specialFamValue;
	}

	public String getSpecialVicValue() {
		return specialVicValue;
	}

	public void setSpecialVicValue(String specialVicValue) {
		this.specialVicValue = specialVicValue;
	}
	
	

}
